//In JavaScript, 
//you can use the includes() method to check whether a string contains a specific substring or not. 
//The includes() method returns a Boolean value (true or false) indicating whether the specified text appears in the string or not.
let myString = "Hello, World!";
let searchString = "World";

if (myString.includes(searchString)) {
  console.log("The string contains the search text.");
} else {
  console.log("The string does not contain the search text.");
}
